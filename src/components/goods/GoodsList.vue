<script>
export default {
  data() {
    return {
      pageindex: 1, // 页码,默认显示第一页
      goodslist: [] // 存储商品数据的数组
    };
  },
  created() {
    this.getGoodsList();
  },
  methods: {
    getGoodsList() {
      this.$http
        .get("api/getgoods?pageindex=" + this.pageindex)
        .then(result => {
          if (result.body.status === 0) {
            // this.goodslist = result.body.message;
            // 防止新数据覆盖老数据 使用 concat 拼接老数据
            this.goodslist = this.goodslist.concat(result.body.message)
          }
        });
    },
    // 点击时 触发 加载更多 商品数据
    getMore(){
      this.pageindex++;
      this.getGoodsList();
    },    
    goDetail(id) {
      // this.$route 和 this.$router 的区别
      // this.$route 是路由[参数对象],所有路由中的参数,params,query都属于它
      // this.$router 是路由[导航对象], 可以使用JS代码,实现路由的前进、后退、跳转到新的URL地址
      // 方法1 最简单的
      // this.$router.push('/home/goodsinfo/'+id)
      // 方法2 传递对象
      // this.$router.push({ path: '/home/goodsinfo/'+id })
      // 方法3 传递命名的路由 这里的name要在路由那里添加name:'goodsinfo'
      this.$router.push({ name: 'goodsinfo', params: { id }})
    }
  }
};
</script>

